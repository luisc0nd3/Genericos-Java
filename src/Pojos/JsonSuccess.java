package Pojos;

public class JsonSuccess {
	private String name;
	
	private String surname;

	public JsonSuccess() {
		super();
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getSurname() {
		return surname;
	}

	public void setSurname(String surname) {
		this.surname = surname;
	}
	
}